# -------------- Comparison Operators --------------

print(12 > 23)  # False
print(14 >= 4)  # True
print(14 < 4)   # False
print(14 == 14)  # True
print(14 == "14")  # False because of their type
print(14 != "14")  # True because they are not equal because of their type
print("cat" > "dog") # False becuase of numeric representation of dog is bigger then num rep of cat
print("cat" == "CAT") # False

